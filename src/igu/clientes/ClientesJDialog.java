    private void jButtonGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGrabarActionPerformed
      Cliente dp= new Cliente();
                        dp.setNombre(v_nombre.getText());
                        dp.setAp_paterno(v_paterno.getText());
                        dp.setAp_materno(v_materno.getText());
                        dp.setCodigo(v_codigo.getText());
                        dp.setUsuario(v_usuario.getText());
                        dp.setPassword(v_contrasenia.getText());
                        
                        if(v_femenino.isSelected()==true){  
                        dp.setSexo("F");
                        }
                        if(v_masculino.isSelected()==true){  
                        dp.setSexo("M");
                        } 
                       if(v_a.isSelected()==true){  
                        dp.setTipo("a");
                      }
                     if(v_b.isSelected()==true){  
                        dp.setTipo("b");
                      }if(v_c.isSelected()==true){  
                        dp.setTipo("c");
                      }if(v_d.isSelected()==true){  
                        dp.setTipo("d");
                      
                      }
                        if(!v_nombre.getText().equals("")|| !v_paterno.getText().equals("") || !v_materno.getText().equals("") || !v_codigo.getText().equals("")  ){
                            
                            Persona pe=metodos.registrarPersona(dp);
                            System.out.println(pe.getId());
                            dp.setPersona_id(pe.getId());
                            
                            ImplCliente implcliente=new ImplCliente();
                            implcliente.registrarCliente(dp);
                            v_nombre.setText("");
                            v_paterno.setText("");
                            v_materno.setText("");
                            v_usuario.setText("");
                            v_contrasenia.setText("");
                            v_codigo.setText("");
                            
                                 JOptionPane.showMessageDialog(
                                        null,
                                        "Se registro conExito", 
                                        "Registro",
                                        JOptionPane.INFORMATION_MESSAGE);
                                 
                                 
                                 
                                 jTablePersonas.removeAll();
                                 ReporteData();
                        }else{
                                 JOptionPane.showMessageDialog(
                                        null,
                                        "No se registro", 
                                        "Error/Advertencia",
                                        JOptionPane.INFORMATION_MESSAGE);
                        }

                        
                        
    }//GEN-LAST:event_jButtonGrabarActionPerformed

